func init() {
	sort.Slice(RegCoins, func(i, j int) bool {
		return RegCoins[i].Name < RegCoins[j].Name
	})
	for i, _ := range RegCoins {
		// https://github.com/satoshilabs/slips/issues/1682
		// Chiliz, Genom, Teslacoin, WaltonChain has invalid values
		if !RegCoins[i].IsValid() {
			RegCoins[i].Type = RegCoins[i].PathComponent % 0x80000000
		}
		RegBip44CoinsTypeToValMap[RegCoins[i].Type] = RegCoins[i]
		RegBip44CoinsNameToValMap[RegCoins[i].Name] = RegCoins[i]
		RegBip44CoinsPathToValMap[RegCoins[i].PathComponent] = RegCoins[i]
	}
}
